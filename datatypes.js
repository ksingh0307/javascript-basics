// javascript dynamic or static

// JavaScript is a dynamically typed language.

// You don't need to declare the data type of a variable explicitly.

// The type is determined at runtime, and a variable can hold values of different types over time.

// Example:

// let x = 10;       // x is a Number
// x = "Hello";      // now x is a String
// x = true;         // now x is a Boolean
// In contrast, statically typed languages (like Java, C++, or TypeScript) require you to
// declare the data type, and variables cannot change types once declared.

// If you want JavaScript with static typing, you can use TypeScript, which is a superset
// of JavaScript that adds optional static typing.
